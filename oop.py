class Calcu:

    def __init__(self, num1, num2, num3):
        self.num1 = num1
        self.num2 = num2
        self.num3 = num3

    def add(self, num4):
        return self.num1 + self.num2 + self.num3 + num4 

    def sub(self):
        return self.num1 - self.num2
        
    def multiply(self):
        return self.num1 * self.num2 

    def divide (self):
        return self.num1 / self.num2 





